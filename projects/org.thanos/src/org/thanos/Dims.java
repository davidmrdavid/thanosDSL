package org.thanos;

public class Dims {

    private long dim1;
    private long dim2;

    public Dims(long dim1, long dim2){
        this.dim1 = dim1;
        this.dim2 = dim2;
    }

    public long getDim1() {
        return dim1;
    }

    public long getDim2() {
        return dim2;
    }
}
